    public void updateTable(Boolean modify_columns) {
        DefaultTableModel model = (DefaultTableModel) jtable_foreings.getModel();
        
        if(modify_columns){
            model.setColumnCount(0);
            
            for(String column : columns_name){
                model.addColumn(column);
            }
        }
        
        model.setRowCount(0);

        for(Object register : registers_list){
            if(register instanceof Facturas){
                Object[] rowData = ((Facturas) register).convertToObjectArray();
                rowData[rowData.length - 2] = null;
                rowData[rowData.length - 1] = null;
                model.addRow(rowData);
            }else if(register instanceof Articulos){
                Object[] rowData = ((Articulos) register).convertToObjectArray();
                rowData[rowData.length - 2] = null;
                rowData[rowData.length - 1] = null;
                model.addRow(rowData);
            }
        }

    }
    
    public void refreshTableTrue(){
        registers_list = session_ctrl.getTableById(id, table);

        if(table.equals("facturas")){
            columns_name = new String[]{"id", "fecha", "total", "metodo de pago"};
        }else{
            columns_name = new String[]{"id", "nombre", "descripci√≥n", "stock"};
        }
        updateTable(true);
        title_table.setText(table);
    }

